SET subtotal = od.quantity * (p.price * (1 - p.discount))
FROM product p 
WHERE p.product_id = od.product_id;

UPDATE orders
SET total = subquery.subtotal
FROM (
    SELECT order_id, SUM(subtotal) as subtotal
    FROM order_details
    GROUP BY order_id
) AS subquery
WHERE orders.order_id = subquery.order_id;